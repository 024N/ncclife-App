package oz.ncclife.layout;

public class RowItemMenuHeader
{
    int id;
    String name;

    public RowItemMenuHeader(int id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public int getId()
    {
        return id;
    }

    public String getName()
    {
        return name;
    }
}